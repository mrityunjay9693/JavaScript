/**
 * Array Length : Array length represents the total number of elements stored/available within an array.
 * length property is used to calculate the length of an array.
 */
//At first, length is calculated and stored in a variable and then print.
let myCars = ["Audi", "Mercedes", "BMW", "Mahindra"];
let lengthOfCars = myCars.length;
console.log("Total number of cars: " + lengthOfCars);

//directly using length property in console.log statement.
let num = [10, 39, 578, 7, 0, 89];
console.log("Total number of values: " + num.length);
console.log(num.length);
